/* ========================================
   3. MAIN INITIALIZATION & EVENTS
   ======================================== */
document.addEventListener('DOMContentLoaded', () => {
    // A. Re-render reviews (just in case)
    renderReviews();

    // B. Navigation Logic
    try {
        const hamburger = document.querySelector('.mobile-menu-btn');
        const navLinksContainer = document.querySelector('.nav-menu');

        if (hamburger && navLinksContainer) {
            hamburger.addEventListener('click', () => {
                hamburger.classList.toggle('active');
                navLinksContainer.classList.toggle('active');

                // Toggle icon
                const icon = hamburger.querySelector('i');
                if (hamburger.classList.contains('active')) {
                    icon.classList.replace('fa-bars', 'fa-times');
                } else {
                    icon.classList.replace('fa-times', 'fa-bars');
                }
            });

            // Close mobile menu when clicking outside
            document.addEventListener('click', (e) => {
                if (!hamburger.contains(e.target) && !navLinksContainer.contains(e.target)) {
                    hamburger.classList.remove('active');
                    navLinksContainer.classList.remove('active');
                    hamburger.querySelector('i').classList.replace('fa-times', 'fa-bars');
                }
            });

            // Smooth Scroll for Navigation Links
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', function(e) {
                    e.preventDefault();
                    hamburger.classList.remove('active');
                    navLinksContainer.classList.remove('active');
                    hamburger.querySelector('i').classList.replace('fa-times', 'fa-bars');

                    const targetId = this.getAttribute('href');
                    if (!targetId || targetId === '#') return;

                    const targetSection = document.querySelector(targetId);
                    if (targetSection) {
                        targetSection.scrollIntoView({
                            behavior: 'smooth'
                        });
                    }
                });
            });
        }
    } catch (error) {
        console.error('Navigation initialization error:', error);
    }



    // C. Scroll Animations
    try {
        const observeElements = () => {
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.style.opacity = '1';
                        entry.target.style.transform = 'translateY(0)';
                    }
                });
            }, {
                threshold: 0.1
            });

            // Note: review-card is purposely excluded to avoid hiding them initially
            document.querySelectorAll('.feature-card, .course-card, .teacher-card').forEach(item => {
                item.style.opacity = '0';
                item.style.transform = 'translateY(30px)';
                item.style.transition = 'all 0.6s ease-out';
                observer.observe(item);
            });
        };
        observeElements();
    } catch (error) {
        console.error('Animation initialization error:', error);
    }
});



